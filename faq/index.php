</p>
<p>
 <b>2. Не могу зайти в личный кабинет. Что делать?</b>
</p>
<p>
	 Скорее всего, при вводе логина или пароля попал лишний пробел. Проверьте, пожалуйста, корректность ввода. Если войти не удалось, обратитесь к Администратору, отправив письмо на адрес: <a href="mailto:bolshoi@abbyy.ru">bolshoi@abbyy.ru</a>.
