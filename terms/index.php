<p>
	 12. Зайдите в личный кабинет и загрузите проверенный пакет, нажав на кнопку «Отправить пакет».
</p>
<p>
	 13. Вычитанный вами пакет в течение 5 (пяти) рабочих дней будет проверен администратором, который удостоверится, что вы не допустили большого количества ошибок и правильно сохранили пакет. Большое количество ошибок в пакете является причиной его отклонения. Результат проверки и начисленные вам баллы вы узнаете в письме, полученном на e-mail, указанный при регистрации.
</p>
<p>
	 14. После отправки пакета на проверку, вы можете взять в работу следующий.
</p>
<p>
 <b>Баллы и рейтинги</b>
</p>
<p>
	 В личном кабинете отражается вся информация об участнике (статус, место в рейтинге, количество вычитанных афиш/программ, баллы), а также все скачанные участником пакеты в хронологическом порядке, начиная с последнего.
</p>
<p>
	 Каждый пакет можно отслеживать по статусу: в работе, на проверке, принят или отклонен.
</p>
<p>
	 За правильно распознанный пакет участник получает 5 баллов. Если пакет отклонен, у участника вычитают 10 баллов. При достижении участником отрицательного рейтинга минус 50 баллов, происходит автоматическая блокировка и доступ к новым заданиям закрывается.
</p>
<p>
 <b><span style="color: red;">Важно! </span></b>Не загружайте на сайт не до конца проверенный пакет, сохраненный в неправильном формате, с вас будут сняты баллы.
</p>
<p>
	 На странице рейтинга отображаются 10 участников, которые набрали наибольшее количество баллов, а также 10 городов с самым большим количеством зарегистрированных участников.
</p>
<p>
 <b><span style="color: red;">Важно!</span></b><b><span style="color: red;"> </span></b>Чем больше пакетов вы успешно проверили, тем больше баллов вы получите и тем выше ваше место в рейтинге.
</p>
<p>
 <b>Требования к верифицированным пакетам</b>
</p>
<p>
	 Верифицированные пакеты не должны содержать ошибок и опечаток. Текст и язык оригинала должны быть сохранены максимально точно.
</p>
<p>
	 Проверенный файл сохраняем как «Документ FineReader» (подробности описаны в инструкции) и архивируем в формате .zip.
</p>
<p>
	 Название загружаемого файла должно полностью повторять изначальное имя пакета.
</p>
<p>
 <b>Второй и третий этапы проверки</b>
</p>
<p>
	 После одобрения администратором вычитанных верификаторами пакетов, они попадают на второй этап проверки.
</p>
<p>
	 На втором этапе аудиторы проводят повторную вычитку пакетов, чтобы исключить пропущенные ошибки.
</p>
<p>
	 Верификатор, чтобы стать аудитором, проходит небольшой тест, который содержит определенное количество вопросов закрытого типа. Правильный вариант всегда один. Тест будет направлен участникам по e-mail, указанный при регистрации, в течение 5 рабочих дней после завершения первого этапа проверки.
</p>
<p>
	 По итогам прохождения теста, если участник правильно ответил на 85% вопросов, он получает статус «Аудитор».
</p>
<p>
	 Система начисления баллов и рейтинга идентична системе первого этапа проверки. Баллы, начисленные за второй этап, суммируются с баллами, начисленными за первый этап.
</p>
<p>
	 После одобрения администратором вычитанных аудиторами пакетов, они попадают на третий этап проверки, где повторяется все заново.
</p>
