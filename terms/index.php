<p>
	 Стать участником волонтерского проекта «Открой историю Большого» может любой желающий вне зависимости от пола, возраста и места проживания. Регистрируясь в качестве участника проекта «Открой историю Большого» (далее «проект»), вы автоматически соглашаетесь с приведенными ниже правилами.
</p>
<p>
 <b>Основные определения</b>
</p>
<p>
	 Афиша – анонс предстоящего театрального события Большого театра. Состоит в основном из одной страницы распознанного текста.
</p>
<p>
	 Программа – листовка или небольшой буклет, содержащий информацию о театральном событии. Чаще всего состоит из нескольких страниц в одном документе.
</p>
<p>
	 Пакет – документ ABBYY FineReader, упакованный в архив .zip и содержащий в себе одну или несколько страниц афиши или программы Большого театра.
</p>
<p>
	 Верификатор<b> </b>– зарегистрированный участник проекта, который принимает участие в 1-м этапе проверки. Проводит первичную вычитку (верификацию) афиш и программ Большого театра, скачанных с сайта проекта.
</p>
<p>
	 Аудитор<b> </b>– зарегистрированный участник проекта, участвует во 2-м и 3-м этапах проверки. Аудитор проводит проверку текстов, вычитанных верификаторами на 1 этапе и другими аудиторами на 2 этапе.
</p>
<p>
	 Администратор – представитель Оргкомитета. Проверяет, принимает или отклоняет пакеты, прошедшие вычитку верификаторами и аудиторами. Начисляет или снимает баллы верификаторам и аудиторам за каждый пакет.
</p>
<p>
 <b>Регистрация, первый этап проверки</b>
</p>
<p>
	 1.&nbsp;&nbsp;&nbsp; Внимательно прочитайте данные условия участия.
</p>
<p>
	 2.&nbsp;&nbsp;&nbsp; Зарегистрируйтесь на сайте проекта, указав логин, пароль, адрес e-mail, а также страну и город проживания. Обязательно укажите свои имя и фамилию – эта информация будет отражена в рейтингах на сайте проекта, а также в вашем личном кабинете. Старайтесь избегать при введении e-mail лишних пробелов, это затруднит повторный вход в личный кабинет.
</p>
<p>
	 3.&nbsp;&nbsp;&nbsp; Проверьте указанный вами адрес e-mail, на него должно прийти письмо с данными и ссылкой для входа в личный кабинет. Если письмо не пришло, проверьте папку «Спам» и запросите его повторно на сайте.
</p>
<p>
	 4.&nbsp;&nbsp;&nbsp; Войдите в свой аккаунт, нажав на кнопку «Вход» на главной странице проекта, и перейдите в личный кабинет.&nbsp;Здесь вы можете отредактировать свой профиль и загрузить вашу фотографию, которая в дальнейшем будет отображаться в личном кабинете и на странице рейтингов. &nbsp;
</p>
<p>
	 5.&nbsp;&nbsp;&nbsp; Внимательно ознакомьтесь с инструкцией в личном кабинете.
</p>
<p>
	 6.&nbsp;&nbsp;&nbsp; Для работы вам понадобится стационарный компьютер на ОС Windows. Работа на Mac, планшетах и других мобильных устройствах не предусмотрена.
</p>
<p>
	 7.&nbsp;&nbsp;&nbsp; Скачайте дистрибутив программы ABBYY&nbsp;FineReader&nbsp;12 Professional Edition в личном кабинете. Для корректной проверки документов мы рекомендуем использовать именно 12-ую версию программы.
</p>
<p>
	 Для установки программы потребуется 800 МБ<b> </b> свободного места на диске на вашем компьютере.
</p>
<p>
	 При установке укажите серийный номер, получить который вы можете в личном кабинете.
</p>
<p>
	 8.&nbsp;&nbsp;&nbsp; Запустите программу и выполните указания из раздела инструкции «Настройка программы для работы».
</p>
<p>
	 9.&nbsp;&nbsp;&nbsp; Можно приступать к проверке текста. Скачайте пакет с документами в личном кабинете. Один пакет содержит от 1 до 10 страниц (редко больше) и занимает около 20 МБ.
</p>
<p>
	 Единовременно у вас на проверке может находиться только один пакет.
</p>
<p>
 <b><span style="color: red;">Важно!</span></b> Выбранный пакет документов нужно проверить и загрузить на сайт <b>в течение 48 часов</b>. По истечении указанного времени незагруженный пакет будет передан на проверку другому участнику.
</p>
<p>
	 10. Проверьте разметку и вычитайте распознанный текст, пользуясь разделами инструкции «Области распознавания», «Проверка текста» и «Особенности верификации».
</p>
<p>
	 Проверка одного пакета занимает в среднем <b>20-50 минут</b>.
</p>
<p>
 <b><span style="color: red;">Важно!</span></b> В текстах афиш и программ Большого театра встречается масса особенностей, которые программа не может распознать корректно. Все они описаны в инструкции, поэтому, пожалуйста, внимательно ознакомьтесь с ней. Текст должен на 100% соответствовать оригиналу.
</p>
<p>
	 Если в процессе проверки у вас возникают вопросы, проверьте, нет ли ответов в инструкции или <a href="http://openbolshoi.p-w-d.ru/faq/">в разделе FAQ</a>. Если ответа нет, напишите письмо на адрес <a href="mailto:bolshoi@abbyy.ru">bolshoi@abbyy.ru</a> или задайте ваш вопрос в группах проекта в <a href="https://vk.com/openbolshoi">ВКонтакте</a> и <a href="https://www.facebook.com/openbolshoi/?ref=aymt_homepage_panel">Facebook</a>, приложив скриншот с описанием проблемы.
</p>
<p>
 <b><span style="color: red;">Важно!</span></b> Не загружайте на сайт пакет, проверенный не до конца.
</p>
<p>
	 11. После завершения проверки сохраните пакет как указано в инструкции в разделе «Завершение проверки». Обратите внимание, что название проверенного пакета должно полностью повторять изначальное имя пакета. То есть если вы, например, скачали пакет «FR_0001_1925», то и сохраненный пакет должен называться «FR_0001_1925».
</p>
<p>
